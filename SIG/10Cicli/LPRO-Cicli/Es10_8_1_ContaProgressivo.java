public class Es10_8_1_ContaProgressivo {
    
    public static void main(String[] args) {
        
        //  da 1 a 10

        // ciclo for
        for(int i = 1; i < 11; i++) {
            System.out.print(i + " ");
        }

        //--
        System.out.println();

        // ciclo do-while
        int i = 1;
        do {
            System.out.print(i + " ");
            i++;
        } while(i <= 10);

        //--
        System.out.println();

        // ciclo while
        // int i = 1;
        i = 1;
        while ( i <= 10) {
            System.out.print(i + " ");
            i++;
        }




    }
}
